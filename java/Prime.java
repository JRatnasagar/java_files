class Prime {
	public static void main(String[] args) {
		int n=5,count=0;
		for(int i=2;i<=n/2;i++){
			if(n%i==0){
				count++;
			}
					if(count==0){

		System.out.println("given number is prime");
					}
		
	else
		System.out.println("given num is not a prime");
	}
				
}
	}

